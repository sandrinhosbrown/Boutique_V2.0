    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ACEPTAR
        int nuevoStock;
        try {
            nuevoStock = (int) jSpinner1.getValue();
            if (nuevoStock < 0) {
                JOptionPane.showMessageDialog(this, "El stock no puede ser negativo",
                        "Stock Incorrecto", JOptionPane.ERROR_MESSAGE);

            } else {
                prendasPasadas.setStock((int) jSpinner1.getValue());
                try {
                    prendaJDBC.actualizarPrenda(prendasPasadas);
                    JOptionPane.showMessageDialog(this, "Actualizado el stock");

                } catch (miExcepcion ex) {
                    JOptionPane.showMessageDialog(this, "El stock no se ha podido actualizar");

                }
                dispose();

            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "NÃºmero de stock incorrecto.",
                    "Stock Incorrecto", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton2ActionPerformed

